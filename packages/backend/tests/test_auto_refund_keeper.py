def test_keeper_respects_cross_border_window() -> None:
    """Cross-border (V2 forward-compat) uses the 14d window."""
    import asyncio

    keeper = AutoRefundKeeper(
        celo=_fake_celo(),
        session_factory=MagicMock(),
        sender=_fake_sender(),
        interval_hours=1.0,
    )

    now = datetime.now(timezone.utc)
    cross_border_order = SimpleNamespace(
        onchain_order_id=7,
        funded_at=now - timedelta(days=10),  # past intra (7d) but not cross (14d)
        is_cross_border=True,
    )

    sent = []

    async def fake_send(onchain_order_id):
        sent.append(onchain_order_id)

    keeper._send_refund_tx = fake_send  # type: ignore[assignment]
    asyncio.run(keeper._maybe_refund(cross_border_order, now))
    assert sent == [], "Cross-border 14d window should not yet have lapsed"
